// Cortexia model catalogue — single source of truth.
// All ~170 kie.ai endpoints, in the new shape used by the DB models table.
// Provider: kie.ai. Cortexia price = provider cost × 1.26.
//
// This file is consumed by:
//   - src/lib/models.ts (UI-facing, exposes new + legacy shape)
//   - drizzle/seed.ts (DB seed)
//   - scripts/generate-seed-sql.mjs (emits drizzle/0002_seed_models.sql)

export type ModelCategory = "image" | "video" | "audio" | "text" | "music";
export type Unit =
  | "image"
  | "second"
  | "1k-chars"
  | "1m-tokens-io"
  | "track";
export type ParamSpec =
  | { kind: "prompt"; label: string; placeholder?: string }
  | { kind: "upload"; label: string; multiple?: boolean; accepts?: string }
  | { kind: "select"; label: string; key: string; options: string[]; advanced?: boolean }
  | {
      kind: "slider";
      label: string;
      key: string;
      min: number;
      max: number;
      step: number;
      default: number;
      suffix?: string;
      advanced?: boolean;
    }
  | { kind: "seed"; label: string; advanced?: boolean }
  | { kind: "toggle"; label: string; key: string; default?: boolean; advanced?: boolean };
export type PriceTier = { label: string; priceUSD: number };
export type FidelityStatus = "fidele" | "generique";

export type InputSchemaField = {
  key: string;
  type: "text" | "longtext" | "upload" | "enum" | "number" | "boolean";
  required?: boolean;
  label?: string;
  options?: string[];
  multiple?: boolean;
  accepts?: string;
  default?: unknown;
  min?: number;
  max?: number;
  step?: number;
};

export type CatalogueEntry = {
  slug: string;
  name: string;
  provider: string;
  category: ModelCategory;
  kieEndpoint: string;
  fidelityStatus: FidelityStatus;
  supportsReferenceUpload: boolean;
  providerCostUsd: number;
  cortexiaPriceUsd: number;
  active: true;
  blurb: string;
  badge?: "popular" | "new" | "pro";
  unit: Unit;
  outputType?: string;
  pricingUnit?: string;
  tiers?: PriceTier[];
  io?: { inputUSD: number; outputUSD: number };
  inputSchema: InputSchemaField[];
};

